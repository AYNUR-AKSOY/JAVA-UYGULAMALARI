public class _my_numberStairs {

	public static void main(String[] args) {
		// nested for loops are loops that are enclosed together
		
		for(int i=1;i<=10;i++){
			
			for(int j=1;j<=i;j++){
			System.out.print(j+" ");
			}
			System.out.println();
			
			}
			
		}
	}


